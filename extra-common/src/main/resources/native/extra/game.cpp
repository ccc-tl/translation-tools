    GameDrawFormattedText(0xF, 0xF, 0x0, 0x0, 0x0, 0x50, text, 0x42, textAlpha << 24, 0, 0x200000, drawList, 0xD, 0, 0, x, y + 1, 1, 1);
    GameDrawFormattedText(0xF, 0xF, 0x0, 0x0, 0x0, 0x50, text, 0x42, textAlpha << 24, 0, 0x200000, drawList, 0xD, 0, 0, x + 1, y, 1, 1);
